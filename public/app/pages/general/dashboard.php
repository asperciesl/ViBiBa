                <h2>New Database Structure</h2>

                <p>Previously every laboratory could directly edit information about a certain sample. While this was
                    certainly the simpler solution it had significant downsides. For instance, with the old system we
                    had problems separating the different (sometimes conflicting) data from the different
                    laboratories.</p>
                <p>Our new approach involves the usage of “Sources”. Every OU (organisational unit, mostly laboratories)
                    can have an unlimited amount of “Sources”. Once set up, an OU can upload data to ViBiBa on
                    its own. You can view all current Sources that are fed into the ViBiBa (just select “Sources” in
                    the menu on the left.</p>

