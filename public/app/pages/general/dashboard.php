                    <p>We are proud to present our newest update for our DETECT CTC Samplebank! In the past months we have spent countless hours  on redesigning the basic principles of the Samplebank to better accommodate our needs.</p>

                    <h2>New Database Structure</h2>

                    <p>Previously every laboratories could directly edit information about a certain sample. While this was certainly the simpler solution it had significant downsides. For instance, with the old system we had problems separating the different (sometimes conflicting) data from the different laboratories.</p>
                    <p>Our new approach involves the usage of “Sources”. Every OU (organisational unit, mostly laboratories) can have an unlimited amount of “Sources”. Once set up, a OU can upload data to the Samplebank on its own. You can view all current Sources that are fed into the Samplebank (just select “Sources” in the menu on the left.</p>

                    <h2>Call for Data</h2>
                    <p>The Samplebank needs your data to continue to grow! Please contact us under probenbank@hhu.de to set up a Source for your data upload. We accept .xlsx and .csv files. You need to at least provide the Kit ID.</p>

                    <h2>Missing Data and Alcedis</h2>
                    <p>Due to the restructuring of the Samplebank some data might no longer be available. This is required, to allow for future data consistency and avoid errors from data from multiple sources.</p>
                    <p>One of our “Sources” is the Alcedis database. If the Samplebank is missing data about a kit, it is almost certainly, because the kit has an empty entry in Alcedis.</p>
                    <p>If you fill out missing data in Alcedis it will be automatically transferred to the Samplebank!</p>
                    <h2>Contact, Bugs and Suggestions</h2>
                    <p>If you find any bugs or have suggestions concerning the Samplebank please contact as via probenbank@hhu.de</p>
